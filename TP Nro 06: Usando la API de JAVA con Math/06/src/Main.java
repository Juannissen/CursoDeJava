public class Main {

    public static void main(String[] args) {
        double numero1 = 20;

        System.out.println("\nFunciones trigonometricas habituales.\n");

        //seno
        System.out.println("operacion math.sin = "+ Math.sin(numero1));
        //coseno
        System.out.println("operacion math.cos = "+ Math.cos(numero1));
        //tangente
        System.out.println("operacion math.tan = "+ Math.tan(numero1));
        //arcotangente
        System.out.println("operacion math.atan = "+ Math.sin(numero1));
        //ATAN2 devuelve el valor de error
        System.out.println("operacion math.atan2 = "+ Math.sin(numero1));

        System.out.println("\nFuncion exponencial y inversa.\n");

        //Exponencial
        System.out.println("operacion math.exp = "+ Math.exp(numero1));
        //Inversa
        System.out.println("operacion math.log = "+ Math.log(numero1));

        System.out.println("\nLas dos constantes PI y e.\n");

        //PI
        System.out.println("Constante math.PI = "+ Math.PI);
        //e
        System.out.println("Constante math.E = "+ Math.E);
    }
}